#!/usr/bin/env node

/**
 * Bundle `apps/desktop/src/api/sandbox/extensionWorkerBootstrap.ts`
 * into a self-contained IIFE string, written to
 * `apps/desktop/src/api/sandbox/extensionWorkerSource.generated.ts`.
 *
 * ## Why a standalone bundle instead of `new Worker(new URL(...))`
 *
 * Next.js 16 / Turbopack + `output: "export"` does not emit worker
 * chunks for `new Worker(new URL("./x", import.meta.url))` the way
 * webpack's classic loader does — the bootstrap ends up as a plain
 * `.ts` asset in `out/_next/static/media/`, which the browser can't
 * execute. Tauri's `file://` webview compounds the problem because
 * cross-file script URLs inside the app bundle behave inconsistently
 * under the custom protocol.
 *
 * Inlining the compiled bundle as a string and spawning the worker
 * from a `Blob` URL sidesteps both issues entirely. The tradeoff is
 * a committed generated file, which we keep behind a header warning
 * so contributors know to re-run this script after editing the
 * bootstrap source.
 *
 * ## Usage
 *
 * `node scripts/build-extension-worker.mjs`
 *
 * Runs esbuild on the bootstrap, wraps the resulting IIFE in a TS
 * string template, and writes the output. Idempotent — rerunning
 * without changes produces a byte-identical file.
 */

import { createRequire } from "node:module";
import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..");

// Resolve `esbuild` out of the desktop package's dependency tree — the
// script lives in /scripts at the repo root, which is not itself a
// package and has no local `node_modules`, so the default ESM resolver
// can't find it. Wire a require rooted inside the desktop package and
// hand it the CommonJS build of esbuild instead.
const requireFromDesktop = createRequire(
  resolve(repoRoot, "apps/desktop/package.json"),
);
const { build } = requireFromDesktop("esbuild");
const desktopSrc = resolve(repoRoot, "apps/desktop/src");
const entryFile = resolve(desktopSrc, "api/sandbox/extensionWorkerBootstrap.ts");
const outFile = resolve(desktopSrc, "api/sandbox/extensionWorkerSource.generated.ts");

const result = await build({
  entryPoints: [entryFile],
  bundle: true,
  write: false,
  format: "iife",
  target: "es2022",
  platform: "browser",
  // A DedicatedWorker runtime has no `window`; mark it explicitly so
  // any esbuild-injected shims stay worker-safe.
  define: {
    "process.env.NODE_ENV": JSON.stringify("production"),
  },
  logLevel: "info",
  // No minification — the readable bundle makes debugging a misbehaving
  // sandboxed extension much easier, and the size difference is
  // negligible for a file that only ships once per install.
});

if (!result.outputFiles || result.outputFiles.length === 0) {
  throw new Error("esbuild produced no output files");
}

const source = result.outputFiles[0].text;

// Template-literal escape: the source may contain backticks, dollar-brace,
// or backslashes. Wrap it in a raw string-literal friendly form by
// escaping those four characters.
const escaped = source
  .replace(/\\/g, "\\\\")
  .replace(/`/g, "\\`")
  .replace(/\$\{/g, "\\${");

const header = `/**
 * AUTO-GENERATED by scripts/build-extension-worker.mjs
 *
 * Do NOT edit by hand. Rerun the script after changing
 * \`extensionWorkerBootstrap.ts\` or any of its imports to regenerate
 * the bundle.
 *
 * The string is the compiled, self-contained IIFE that runs inside
 * the extension sandbox worker. The host spawns a \`Worker\` from a
 * \`Blob\` URL wrapping this source.
 */

export const EXTENSION_WORKER_SOURCE = \`${escaped}\`;
`;

writeFileSync(outFile, header, "utf8");
console.log(`[build-extension-worker] wrote ${outFile} (${source.length} bytes of JS)`);
